                        #Tipos de datos
#NUMBER
variable_int = 1
print("Numero entero:",variable_int)

variable_float = 1.5
print("Numero flotante:",variable_float)

variables_int = "1,2,3,4,5"
print(variables_int)

variable_complex = "4j, 1 + 2i"
print("Numeros complejos:",variable_complex)

variable_bool_F = False
variable_bool_T = True
#Concatenacion de variables en un print
print("Booleanos:", variable_bool_F,"," ,variable_bool_T)

#"type" me indica el tipo de variable
print(type(variable_bool_F))

#STRINGS
variable_str = 'Esta es una cadena'
print(variable_str)

#Uso de "str" para la conversion de un numero entero a cadena
