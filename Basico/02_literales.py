#Literales
#Un literal se refiere a datos cuyos valores están determinados por el literal mismo.

#Enteros
print("2") #Cadena
print(2)    #Numero entero

#Números octales
'''Si un número entero esta precedido por un código 0O o 0o (cero-o), 
el número será tratado como un valor octal'''
print(0O507)
print(0o123)

#Números hexadecimales
'''Dichos números deben ser precedidos por el prefijo 0x o 0X (cero-x).'''
print(0X507)
print(0x123)

#Flotantes

#Notacion Cientifica
''' 3 x 10^8 =300000000
Tambien se puede lograr con = 3E8
La letra  E o e - proviene de la palabra exponente,
la cual significa por diez a la n potencia
'''
print(3E8)
print(6.62607E-4)

#Python siempre elige la presentación más corta del número
print(0.0000000000000000000001)


